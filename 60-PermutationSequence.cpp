/*

The set [1,2,3,â€¦,n] contains a total of n! unique permutations.

By listing and labeling all of the permutations in order,
We get the following sequence (ie, for n = 3):

"123"
"132"
"213"
"231"
"312"
"321"
Given n and k, return the kth permutation sequence.

Note: Given n will be between 1 and 9 inclusive.

*/

class Solution {
public:
    string getPermutation(int n, int k) {
        vector<int> nums;
        int total = 1;
        for (int i = 1; i <= n; ++i) {
            nums.emplace_back(i);
            total *= i;
        }
        //assuming k is correct i.e. 1<= k <= n!; return "" for k = 0
        string res;
        while(n) {
            total /= n;
            int id = (k-1)/total;
            res += to_string(nums[id]);
            nums.erase(nums.begin() + id); //key point in this question
            k -= id * total; // following line will also work
            // k = (k - 1) % total + 1;
            --n;
        }
        return res;
    }
};

